// Function to switch language
function switchLanguage(language) {
    const translation = translations[language];

    // Contact Button
    contactButton.textContent = translation.contactButton;

    // View Button
    viewButtons.forEach(button => {
        button.textContent = translation.viewButton;
    });

    // Learn more links
    learnMoreLinks.forEach(link => {
        // Update only the text while preserving the icon
        const iconHTML = link.querySelector("i").outerHTML;  // Get the icon's HTML
        link.innerHTML = `${translation.learnMoreLink} ${iconHTML}`; // Update the text and keep the icon
    });

    // --------- Navbar ---------
    navHome.textContent = translation.navLinkHome;
    navAbout.textContent = translation.navLinkAbout;
    navService.textContent = translation.navLinkService;
    
    // --------- Header ---------
    headerLink.textContent = translation.headerLink;
    headerTitle.textContent = translation.headerTitle;
    headerDescription.textContent = translation.headerDescription;

    // --------- About ---------
    aboutMainTitle.textContent = translation.aboutMainTitle;
    aboutMainDesc.textContent = translation.aboutMainDesc;
    aboutCardTitle1.textContent = translation.aboutCardTitle1;
    aboutCardTitle2.textContent = translation.aboutCardTitle2;
    aboutCardTitle3.textContent = translation.aboutCardTitle3;
    aboutCardDesc1.textContent = translation.aboutCardDesc1;
    aboutCardDesc2.textContent = translation.aboutCardDesc2;
    aboutCardDesc3.textContent = translation.aboutCardDesc3;

    // --------- Our Services --------- 
    serviceSubheading.textContent = translation.serviceSubheading;
    serviceMainTitle.textContent = translation.serviceMainTitle;
    serviceMainDesc.textContent = translation.serviceMainDesc;
    serviceCardTitle1.textContent = translation.serviceCardTitle1;
    serviceCardDesc1.textContent = translation.serviceCardDesc1;
    serviceCardTitle2.textContent = translation.serviceCardTitle2;
    serviceCardDesc2.textContent = translation.serviceCardDesc2;
    serviceCardTitle3.textContent = translation.serviceCardTitle3;
    serviceCardDesc3.textContent = translation.serviceCardDesc3;
    serviceCardTitle4.textContent = translation.serviceCardTitle4;
    serviceCardDesc4.textContent = translation.serviceCardDesc4;

    // --------- Numbers ---------
    numbersExperienceText.textContent = translation.numbersExperienceText;
    numbersProjectText.textContent = translation.numbersProjectText;
    numbersClientText.textContent = translation.numbersClientText;

    // --------- Clients ---------
    clientsMainTitle.textContent = translation.clientsMainTitle;

    // --------- Footer ---------
    footerBrandDescription.textContent = translation.footerBrandDescription;
    const iconHTML = footerLocation.querySelector("i").outerHTML;  // Get the icon's HTML
    footerLocation.innerHTML = `${iconHTML} ${translation.footerLocation}`; // Update the text and keep the icon

    // Store the selected language in localStorage
    localStorage.setItem('selectedLanguage', language);
}

// Event listener for language selection
languageSelector.addEventListener("change", function () {
    const selectedLanguage = this.value;
    switchLanguage(selectedLanguage);
});

// Function to get the saved language from localStorage
function getSavedLanguage() {
    const savedLanguage = localStorage.getItem('selectedLanguage');
    return savedLanguage ? savedLanguage : 'uz';  // Default to English if nothing is saved
}

// Initialize the language based on localStorage or default to English
const initialLanguage = getSavedLanguage();
languageSelector.value = initialLanguage;  // Set the dropdown value to match the selected language
switchLanguage(initialLanguage);
